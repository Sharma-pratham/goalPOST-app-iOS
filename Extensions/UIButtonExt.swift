import UIKit
extension UIButton {
    func setSelectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.3097702216, green: 0.6003848376, blue: 1, alpha: 1)
    }
    
    func setDeslectedColor() {
        self.backgroundColor = #colorLiteral(red: 0.4572432793, green: 0.5924020195, blue: 0.8705882353, alpha: 1)
    }
    
}
